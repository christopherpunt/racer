def twoLinesCollided(line1, line2):
    xdiff = (line1.line.x - line1.line.y, line2.x1 - line2.y1)
    ydiff = (line1.line.x2 - line1.line.y2, line2.x2 - line2.y2)

    def det(a, b):
        return a[0] * b[1] - a[1] * b[0]

    div = det(xdiff, ydiff)
    if div == 0:
       return

    d = (det(*line1), det(*line2))
    x = det(d, xdiff) / div
    y = det(d, ydiff) / div
    return x, y
